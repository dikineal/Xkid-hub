local function doFire(path, argsStr)
    if not path or path == "" then
        Library:Notification("вќЊ ERROR", "Path remote kosong!", 2)
        return
    end
    
    local remote = findByPath(path)
    if not remote then
        Library:Notification(
            "вќЊ ERROR",
            "Remote tidak ditemukan:\n" .. path,
            4
        )
        return
    end
    
    local args = parseArgs(argsStr)
    local argsDisplay = #args > 0 and table.concat(args, ", ") or "(none)"
    
    if remote:IsA("RemoteEvent") then
        local ok, err = pcall(function()
            remote:FireServer(table.unpack(args))
        end)
        
        if ok then
            Library:Notification(
                "вњ… FIRESERVER BERHASIL",
                string.format("%s\nрџ“¦ Args: %s", remote.Name, argsDisplay),
                3
            )
        else
            Library:Notification(
                "вќЊ FIRESERVER GAGAL",
                tostring(err),
                4
            )
        end
        
    elseif remote:IsA("RemoteFunction") then
        local ok, res = pcall(function()
            return remote:InvokeServer(table.unpack(args))
        end)
        
        if ok then
            Library:Notification(
                "вњ… INVOKESERVER BERHASIL",
                string.format("%s\nрџ“¦ Result: %s", remote.Name, serializeValue(res, 0)),
                4
            )
        else
            Library:Notification(
                "вќЊ INVOKESERVER GAGAL",
                tostring(res),
                4
            )
        end
        
    else
        Library:Notification(
            "вќЊ ERROR",
            "Objek bukan RemoteEvent/Function",
            3
        )
    end
end

